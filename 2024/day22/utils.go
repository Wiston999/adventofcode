func (p *Problem) Evolve(input int) (result int) {
	tmp1 := ((input * 64) ^ input) % 16777216
	tmp2 := ((tmp1 / 32) ^ tmp1) % 16777216
	result = ((tmp2 * 2048) ^ tmp2) % 16777216
	return
}

func (p *Problem) Part1() (result int) {
	for _, s := range p.Secrets {
		for i := 0; i < 2000; i++ {
			s = p.Evolve(s)
		}
		result += s
	}
	return
}

func (p *Problem) Part2() (result int) {
	partials := make(map[Sequence]int)
	for _, s := range p.Secrets {
		secretDiff := make([]int, 0)
		visited := make(map[Sequence]void)
		for i := 0; i < 2000; i++ {
			n := p.Evolve(s)
			secretDiff = append(secretDiff, (n%10)-(s%10))
			s = n
			if i > 3 {
				seq := Sequence{secretDiff[i-3], secretDiff[i-2], secretDiff[i-1], secretDiff[i]}
				if _, ok := visited[seq]; !ok {
					partials[seq] += n % 10
				}
				visited[seq] = null
			}
		}
	}
	for _, v := range partials {
		if v > result {
			result = v
		}
	}
	return
}

func NewProblem(ctx *cli.Context) (p *Problem, err error) {
	input := ctx.String("input")
	byteData, err := os.ReadFile(input)
	if err != nil {
		log.Error(fmt.Sprintf("Error opening file %s for reading input: %v", input, err))
		return
	}
	p = new(Problem)
	strData := string(byteData)
	for i, l := range strings.Split(strings.TrimSpace(strData), "\n") {
		log.Debug(fmt.Sprintf("Parsing line %03d: %s", i, l))
		p.Secrets = append(p.Secrets, atoi(l))
	}
	return
}

func setLogLevel(level string) {
	switch strings.ToLower(level) {
	case "debug":
		log.SetLevel(log.DebugLevel)
	case "info":
		log.SetLevel(log.InfoLevel)
	case "warn":
		log.SetLevel(log.WarnLevel)
	case "error":
		log.SetLevel(log.ErrorLevel)
	default:
		log.Fatal(fmt.Sprintf("Unkown log level %s", level))
	}
}

